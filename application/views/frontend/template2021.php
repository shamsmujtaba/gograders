  <script>

    const phrases = [

      " CONFIDENTIAL HELP",

      " RAPID SOLUTIONS ",

    //   " Affordable Services"

    ];



    const typingSpeed = 100; // Adjust typing speed (in milliseconds)

    const pauseDuration = 1500; // Duration to pause after text is fully typed (in milliseconds)



    const typingTextElement = document.getElementById("typing-text");

    const cursorElement = document.getElementById("cursor");



    let currentPhraseIndex = 0;

    let currentIndex = 0;

    let isDeleting = false;



    function typeAndRemoveText() {

      const currentPhrase = phrases[currentPhraseIndex];

      const currentText = currentPhrase.substring(0, currentIndex);



      typingTextElement.textContent = currentText;



      if (isDeleting) {

        currentIndex--;

      } else {

        currentIndex++;

      }



      if (currentIndex === currentPhrase.length + 1) {

        isDeleting = true;

        cursorElement.style.opacity = 0;

        setTimeout(() => {

          isDeleting = false;

          currentIndex = 0;

          cursorElement.style.opacity = 1;



          // Move to the next phrase

          currentPhraseIndex = (currentPhraseIndex + 1) % phrases.length;

        }, pauseDuration);

      }



      setTimeout(typeAndRemoveText, isDeleting ? typingSpeed / 2 : typingSpeed);

    }



    setTimeout(typeAndRemoveText, typingSpeed); // Start after "We should" is displayed

  </script>



